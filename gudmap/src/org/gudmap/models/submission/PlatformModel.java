package org.gudmap.models.submission;

public class PlatformModel {
	
	 private String geoID;
	    private String title;
	    private String name;
	    private String distribution;
	    private String technology;
	    private String organism;
	    private String manufacturer;
	    private String manufactureProtocol;
	    private String catNo;

	    public void setGeoID(String geoID) {
	        this.geoID = geoID;
	    }

	    public String getGeoID() {
	        return geoID;
	    }

	    public void setTitle(String title) {
	    	this.title = title;
	    }

	    public String getTitle() {
	        return title;
	    }

	    public void setName(String name) {
	    	this.name = name;
	    }

	    public String getName() {
	        return name;
	    }

	    public void setDistribution(String distribution) {
	    	this.distribution = distribution;
	    }

	    public String getDistribution() {
	        return distribution;
	    }

	    public void setTechnology(String technology) {
	    	this.technology = technology;
	    }

	    public String getTechnology() {
	        return technology;
	    }

	    public void setOrganism(String organism) {
	    	this.organism = organism;
	    }

	    public String getOrganism() {
	        return organism;
	    }

	    public void setManufacturer(String manufacturer) {
	    	this.manufacturer = manufacturer;
	    }

	    public String getManufacturer() {
	        return manufacturer;
	    }

	    public void setManufactureProtocol(String manufactureProtocol) {
	    	this.manufactureProtocol = manufactureProtocol;
	    }

	    public String getManufactureProtocol() {
	        return manufactureProtocol;
	    }

	    public void setCatNo(String catNo) {
	    	this.catNo = catNo;
	    }

	    public String getCatNo() {
	        return catNo;
	    }


}
